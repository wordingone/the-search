class Cell:
    """
    A self-referential computational unit.

    State x ∈ ℝ^d IS the dynamics:
    - x determines Φ through the cross-coupling term
    - Φ determines the next x
    - The loop never opens

    The cell maintains a short trajectory history to measure
    its own dynamical character (settled vs active vs chaotic).
    """
    __slots__ = ('d', 'x', 'history', 'hist_len')

    def __init__(self, d, scale=0.5):
        self.d = d
        self.x = vrand(d, scale)
        self.hist_len = 12
        self.history = [self.x[:]]

    def record(self):
        self.history.append(self.x[:])
        if len(self.history) > self.hist_len:
            self.history.pop(0)

    def activity(self):
        """How dynamically active this cell is (recent trajectory energy)."""
        return trajectory_energy(self.history, min(8, len(self.history)))

    def fp_dist(self, alpha, beta):
        return fixed_point_distance(self.x, alpha, beta)

    def plasticity(self, alpha, beta, sigma=0.15):
        """
        High near fixed points (paradoxically: settled cells are RECEPTIVE).
        Low when far from any fixed point (active cells resist perturbation).

        This is the opposite of RK's autonomy. A cell that has found
        self-consistency is OPEN to the world. A cell in turmoil is closed.

        Why? Because a cell at a fixed point has spare capacity.
        A cell actively computing needs all its dynamics for itself.
        """
        d = self.fp_dist(alpha, beta)
        return math.exp(-(d * d) / (sigma * sigma))


