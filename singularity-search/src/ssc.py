# §2  THE SELF-REFERENTIAL MAP
# ═══════════════════════════════════════════════════════════════

def phi(x, alpha, beta):
    """
    Φ(x)_k = tanh(α·x_k + β·x_{k+1}·x_{k-1})

    The state determines the dynamics through the product term.
    The product x_{k+1}·x_{k-1} gates dimension k based on its
    neighbors' values. This creates SPATIAL STRUCTURE within a
    single state vector — structure that was never programmed.

    α > 1: zero is unstable. The system MUST find non-trivial states.
    β > 0: cross-coupling creates correlations across dimensions.
    """
    d = len(x)
    out = [0.0] * d
    for k in range(d):
        kp = (k + 1) % d
        km = (k - 1) % d
        out[k] = math.tanh(alpha * x[k] + beta * x[kp] * x[km])
    return out


def phi_cross(x_self, x_other, alpha, beta):
    """
    Φ_self(other): cell applies its own structure to another's state.

    Φ_self(y)_k = tanh(α·y_k + β·x_self_{k+1} · y_{k-1})

    The 'program' (cross-coupling pattern) comes from x_self.
    The 'data' comes from x_other.
    """
    d = len(x_self)
    out = [0.0] * d
    for k in range(d):
        kp = (k + 1) % d
        km = (k - 1) % d
        out[k] = math.tanh(alpha * x_other[k] + beta * x_self[kp] * x_other[km])
    return out


def fixed_point_distance(x, alpha, beta):
    """‖Φ(x) - x‖ / max(‖x‖, 1). How far from self-consistency."""
    p = phi(x, alpha, beta)
    diff = vsub(p, x)
    return vnorm(diff) / max(vnorm(x), 1.0)


def trajectory_energy(history, window=5):
    """Average displacement over recent steps. Low = settled. High = active."""
    if len(history) < 2:
        return 0.0
    recent = history[-window:]
    total = 0.0
    for i in range(1, len(recent)):
        total += vnorm(vsub(recent[i], recent[i-1]))
    return total / (len(recent) - 1)


