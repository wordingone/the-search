class Engine:
    """
    N self-referential cells coupled through state-derived topology.

    Update for cell i:
      raw_i = Φ(x_i)                                    (self-application)
      pull_i = Σ_j a_ij · [Φ_i(x_j) − Φ(x_i)]         (coupling)
      x_i' = raw_i + p_i · ε · pull_i + signal          (integration)

    Where:
      p_i = plasticity (high at fixed points, low during activity)
      a_ij = softmax(x_i · x_j / dτ)  (state-similarity coupling)
      ε = coupling strength

    The mixing rate δ smooths the update:
      x_i(t+1) = (1−δ)·x_i(t) + δ·x_i'
    """

    def __init__(self, n=8, d=12, alpha=1.15, beta=0.5, seed=None):
        if seed is not None:
            random.seed(seed)
        self.n = n
        self.d = d
        self.alpha = alpha
        self.beta = beta
        self.cells = [Cell(d, scale=0.5) for _ in range(n)]
        self.epsilon = 0.3   # coupling strength
        self.tau = 0.4       # attention temperature
        self.delta = 0.4     # update smoothing
        self.noise = 0.005   # prevents stagnation
        self.t = 0
        self.history = []    # system-level trajectory

    def coupling_weights(self, i):
        """a_ij = softmax(x_i · x_j / (d·τ)), j ≠ i."""
        xi = self.cells[i].x
        n, d, tau = self.n, self.d, self.tau
        raw = []
        for j in range(n):
            if i == j:
                raw.append(-1e10)
            else:
                raw.append(vdot(xi, self.cells[j].x) / (d * tau))
        mx = max(raw)
        exps = [math.exp(min(r - mx, 50)) for r in raw]
        s = sum(exps) + 1e-15
        return [e / s for e in exps]

    def step(self, signal=None, signal_strength=0.3):
        """One integration step."""
        n, d = self.n, self.d
        alpha, beta = self.alpha, self.beta
        cells = self.cells

        new_states = []

        for i in range(n):
            ci = cells[i]

            # Self-application: the cell's own dynamics
            raw = phi(ci.x, alpha, beta)

            # Coupling: neighbors' programs applied to this cell
            pi = ci.plasticity(alpha, beta)
            if pi > 0.01 and self.epsilon > 0:
                weights = self.coupling_weights(i)
                pull = vzero(d)
                for j in range(n):
                    if i == j:
                        continue
                    w = weights[j]
                    if w < 1e-8:
                        continue
                    cross = phi_cross(cells[j].x, ci.x, alpha, beta)
                    diff = vsub(cross, raw)
                    pull = vadd(pull, vscale(diff, w))
                raw = vadd(raw, vscale(pull, pi * self.epsilon))

            # External signal: enters through plasticity gate
            if signal is not None:
                raw = vadd(raw, vscale(signal, pi * signal_strength))

            # Small noise to prevent perfect stagnation
            raw = vadd(raw, vrand(d, self.noise))

            # Smooth update
            new_x = vadd(vscale(ci.x, 1 - self.delta), vscale(raw, self.delta))
            new_states.append(new_x)

        # Apply all updates simultaneously
        for i in range(n):
            cells[i].x = new_states[i]
            cells[i].record()

        self.t += 1

    def inject(self, pattern, cell_indices=None):
        """Set cell states directly (for testing attractor basins)."""
        if cell_indices is None:
            cell_indices = range(self.n)
        for i in cell_indices:
            self.cells[i].x = pattern[:]
            self.cells[i].history = [pattern[:]]

    # ── Observables ──────────────────────────────────────────

    def mean_fp_dist(self):
        return sum(c.fp_dist(self.alpha, self.beta) for c in self.cells) / self.n

    def mean_activity(self):
        return sum(c.activity() for c in self.cells) / self.n

    def mean_plasticity(self):
        return sum(c.plasticity(self.alpha, self.beta) for c in self.cells) / self.n

    def mean_norm(self):
        return sum(vnorm(c.x) for c in self.cells) / self.n

    def centroid(self):
        """Mean state across all cells."""
        c = vzero(self.d)
        for cell in self.cells:
            c = vadd(c, vscale(cell.x, 1.0 / self.n))
        return c

    def dispersion(self):
        """How spread out are cell states."""
        c = self.centroid()
        return sum(vnorm(vsub(cell.x, c))**2 for cell in self.cells) / self.n

    def snapshot(self):
        return [c.x[:] for c in self.cells]

    def measure(self):
        return dict(
            t=self.t,
            fp=round(self.mean_fp_dist(), 4),
            act=round(self.mean_activity(), 5),
            pla=round(self.mean_plasticity(), 4),
            nrm=round(self.mean_norm(), 4),
            dis=round(self.dispersion(), 4))


