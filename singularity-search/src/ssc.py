"""
Self-Sustaining Computation

Every computational architecture has a frozen level — a level where
structure is GIVEN, never GENERATED by the system itself:

  Transformers: weights are frozen during inference.
  SSMs: state dynamics are parameterized, never self-produced.
  Gradient descent: the loss function never changes.

A living system has no frozen level. Its structure is continuously
produced by the very processes that the structure enables.

This is not a metaphor. It is a mathematical condition:

  SELF-REFERENCE: The state determines the dynamics.
  SUSTAINED DISEQUILIBRIUM: The system never reaches equilibrium.
  TRAJECTORY AS COMPUTATION: The attractor basin IS the output.

The mathematical realization:

  State: x ∈ ℝ^d, arranged as a circular lattice.

  Self-referential map:
    Φ(x)_k = tanh(α·x_k + β·x_{k+1}·x_{k-1})

  Two terms only:
    α·x_k          — self-drive (α > 1 → zero is unstable)
    β·x_{k+1}·x_{k-1} — cross-dimensional coupling
                        (neighbors multiply to gate each dimension)

  The state determines the dynamics (through β·x_{k+1}·x_{k-1}).
  The dynamics determine the next state.
  The loop closes. There is no external program.

  Multi-cell coupling:
    x'_{i,k} = Φ(x_i)_k + ε · Σ_j a_ij · (Φ_i(x_j)_k − Φ(x_i)_k)

  Where:
    Φ_i(y)_k = tanh(α·y_k + β·x_{i,k+1}·y_{k-1})
    (cell i applies its own structure to neighbor j's state)

    a_ij = exp(x_i·x_j / (d·τ)) / Z_i
    (coupling strength = state similarity, self-organized topology)

  A cell near a fixed point of its own Φ resists coupling (autonomy).
  A cell far from any fixed point is shaped by neighbors (plasticity).
  This isn't designed. It falls out of the dynamics.

  Computation: different inputs → different attractor basins → different outputs.
  Adaptation: past inputs reshape the attractor landscape.
  Homeostasis: perturbation followed by return to the active trajectory.

  No loss function. No optimizer. No training/inference split.
  No dependencies. Pure Python. The code IS the math.
"""

