def bar(v, w=20, lo=0.0, hi=1.0):
    f = max(0.0, min(1.0, (v - lo) / (hi - lo + 1e-10)))
    n = int(f * w)
    return '█' * n + '░' * (w - n)

def cosine(a, b):
    return vcosine(a, b)

